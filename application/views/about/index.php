                <h3 class="pb-3 fw-bold">History</h3>
                <p >1986 - The Ottawa Charter for Health Promotion set the international principles of health promotion and inspired countries around the world to focus on public health, areas for priority action, and commit to health promotion</p>
                <p >1992 - 4th ASEAN Summit urged member states to promote "Human resource development so as to further strengthen the existing network of leading universities and institutions of higher learning in the region"</p>
                <p >1995 - ASEAN University Network (AUN)was established</p>
                <p >2014 - AUN-HPN was established as thematic network of AUN, and has been serving as a regional network of people and universities working towards a better and healthier society.</p>
                <h3 class="pb-3 fw-bold">Vision</h3>
                <p >ASEAN Universities are leading institutions in promoting health within universities and beyond.</p>
                <h3 class="pb-3 fw-bold">Mission</h3>
                <ul>
                    <li>To serve as a platform for ASEAN higher education institutions for collaboration among
                        themselves and with other key stakeholders for the purpose of health promotion in the
                        ASEAN region.</li>
                    <li>To share knowledge, skills, and resources among the network members in order to build
                        capacity for health promotion and to develop innovative solutions to common health
                        challenges in the region.</li>
                    <li>To set standards for health promotion universities for health promotion universities in the
                        ASEAN region . To put to task various sectors of universities in the ASEAN region to address the threats of
                        unhealthy behaviors and poor environment that impact health of university personnel,
                        students and the society as a whole.</li>
                    <li>To promote the role of AUN member universities in the area of health promotion within and
                        outside the universities. </li>
                    <li>To scale-up health promotion activities in all ASEAN universities</li>
                </ul>
                <h3 class="pb-3 fw-bold">OBJECTIVE</h3>
                <ul>
                    <li>
                        To provide a discussion platform for stakeholders (policymakers, health care professionals,
                        university students, communities, and dialogue partners)involved in health promotion activities within the ASEAN region for intellectual exchange and experience-sharing on the successes, learnings, and best practices. To document outcomes of current health promotion policies and practices being implemented by participating universities, with a view to promoting effective health
                        promotion policies and practices in cross-border collaboration among ASEAN universities
                    </li>
                    <li>
                        To promote the roles of universities in health promotion, particularly on action-oriented
                        policies that relate to community and public health.
                    </li>
                    <li>
                        To proactively promote platforms for knowledge sharing in the area of health promotion
                    </li>
                    <li>
                        To make recommendations for further development of the collaboration framework on
                        interdisciplinary health promotion in the South East Asian region.
                    </li>
                </ul>
                <h3 class="pb-3 fw-bold">OUTCOMES</h3>
                <ul>
                    <li>
                        Member universities become health-promoting universities and act as health promoters for
                        the whole society
                    </li>
                    <li>
                        Member universities play the leadership role in health promotion in their countries.
                    </li>
                    <li>
                        Member universities set up a policy-linked research center for evidence-based
                        policymaking to mitigate health problems in their countries.
                    </li>
                </ul>
